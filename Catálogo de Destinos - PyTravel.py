from docx import Document

# FunÃ§Ã£o para extrair cidades a partir de uma seÃ§Ã£o do documento
def extrair_cidades(secao_texto):
    linhas = secao_texto.split('\n')
    cidades = []
    for linha in linhas:
        if '(' in linha and ')' in linha:
            cidades.append(linha.strip())
    return cidades

